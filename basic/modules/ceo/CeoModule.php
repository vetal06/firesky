<?php

namespace app\modules\ceo;

use yii\filters\AccessControl;

/**
 * ceo module definition class
 */
class CeoModule extends \yii\base\Module
{
    /**
     * @inheritdoc
     */
    public $controllerNamespace = 'app\modules\ceo\controllers';

    /**
     * layout админ части модуля
     * @var
     */
    public $adminLayout;

    public $adminRoles = ['@'];

    /**
     * Список параметров которіе учитывать в формировании url
     * @var
     */
    public $urlParams = [];

    public function beforeAction($action)
    {
        if(preg_match('/admin/', $action->controller->id)) {
            $this->layout = $this->adminLayout;
        }
        return parent::beforeAction($action); // TODO: Change the autogenerated stub
    }

    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'rules' => [
                    [
                        'allow' => true,
                        'roles' => $this->adminRoles,
                    ]
                ],
            ],
        ];
    }
    /**
     * @inheritdoc
     */
    public function init()
    {
        parent::init();

        // custom initialization code goes here
    }
}
